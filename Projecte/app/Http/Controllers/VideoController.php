    public function ChangeVideo(Request $request) {
        $video = new Video;
        $video->setAttribute("room_id", $request->room);
        $video->setAttribute("user_id", Auth::user()->id);
        $video->setAttribute("link", $request->video);
        $video->save();

        event(new ChangedVideo($video));

        return back();
    }

